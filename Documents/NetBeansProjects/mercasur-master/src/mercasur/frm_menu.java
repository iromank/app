    private void txt_j_montoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_j_montoKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            String monto = txt_j_monto.getText();
            if (c_varios.esDecimal(monto)) {
                double dmonto = Double.parseDouble(monto);
                if (dmonto >= 0) {
                    btn_aperturar.setEnabled(true);
                    btn_aperturar.requestFocus();
                }
            } else {
                txt_j_monto.setText("");
                txt_j_monto.requestFocus();
            }
        }
    }//GEN-LAST:event_txt_j_montoKeyPressed


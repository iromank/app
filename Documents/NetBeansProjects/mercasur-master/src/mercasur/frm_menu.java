    private void btn_ingresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ingresarActionPerformed
        jd_login.setVisible(false);
        c_empleado.cargar_datos();
        c_zona.cargar_datos();
        Notification.show("Bienvenido", "BIENVENIDO SR(A): " + c_empleado.getNombres() + "  " + c_empleado.getApe_pat() + " " + c_empleado.getApe_mat());
        lbl_empleado.setText(c_empleado.getNombres() + "  " + c_empleado.getApe_pat() + " " + c_empleado.getApe_mat());
        lbl_zona.setText(c_zona.getNombre() + " | " + c_zona.getCiudad());
        c_caja.setFecha(c_varios.getFechaActual());
        
        if (!c_caja.validar_apertura()) {
            jd_apertura.setSize(402, 225);
            jd_apertura.setModal(true);
            txt_j_usuario.setText(c_empleado.getNombres() + "  " + c_empleado.getApe_pat() + " " + c_empleado.getApe_mat());
            txt_j_fecha.setText(c_varios.formato_fecha_vista(c_varios.getFechaActual()));
            txt_j_monto.setText("");
            txt_j_monto.requestFocus();
            jd_apertura.setLocationRelativeTo(null);
            jd_apertura.setVisible(true);
        } else {
            if (c_caja.getCierre() > 0) {
                JOptionPane.showMessageDialog(null, "YA CERRO CAJA, NO PUEDE ABRIR EL SISTEMA");
                System.exit(0);
            }
        }
    }//GEN-LAST:event_btn_ingresarActionPerformed

    private void txt_contrasenaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_contrasenaKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (txt_contrasena.getText().length() > 0) {
                String password = String.valueOf(txt_contrasena.getPassword());
                if (c_empleado.getContrasena().equals(password)) {
                    txt_usuario.setEnabled(false);
                    txt_contrasena.setEnabled(false);
                    cbx_zonas.setEnabled(true);
                    cbx_zonas.requestFocus();
                } else {
                    Notification.show("Login", "Contraseña Invalida", Notification.ERROR_MESSAGE);
                    txt_contrasena.setText("");
                    txt_contrasena.requestFocus();
                }
            }
        }
    }//GEN-LAST:event_txt_contrasenaKeyPressed

